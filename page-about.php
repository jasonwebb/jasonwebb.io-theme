  <h1 class="visually-hidden">About me</h1>

  <img src="<?= get_template_directory_uri(); ?>/images/pages/about/portrait.png" class="portrait is-pulled-right" alt="Jason teaching a DIY synthesizer workshop">

  <p>Jason is a creative technologist based in Minneapolis, MN who continually explores the boundaries and intersections of creativity, technology, fabrication, science, and the natural world through experimental project-based work.</p>
  <p>In his personal research he is particularly focused on the exploration and application of biological processes relating to form using computational design, digital fabrication, and electronics.</p>
  <p>Professionally he leverages these passions as a UI developer specializing in cross-disciplinary collaboration between design and technical teams, realizing modern and clean experiences for all kinds of users.</p>
  <p>These interests have afforded him a few interesting opportunities over the years, including:</p>

  <ul class="highlights">
    <li>Teaching workshops on <a href="https://jasonwebb.io/2015/08/circuit-bending-workshop-at-omaha-under-the-radars-soundry-camp/" aria-label="circuit bending workshop at SOUNDRY">circuit bending</a> and <a href="https://jasonwebb.io/2017/07/atari-punk-console-synthesizer-build-workshop-for-soundry-camp/" aria-label="DIY synth workshop at SOUNDRY">building DIY synthesizers</a> to young adults through <a href="https://www.undertheradaromaha.com/soundry-workshop" target="_blank">SOUNDRY</a>, an educational outreach component of the <a href="https://www.undertheradaromaha.com/" target="_blank">Omaha Under the Radar Festival</a>.</li>

    <li>Collaborating with and providing creative technology guidance and support to nationally- and internationally-recognized fine artists through the <a href="https://www.bemiscenter.org" target="_blank">Bemis Center for Contemporary Art</a> in Omaha, NE. Collaborators included <a href="http://emiliebaltz.com/" target="_blank">Emilie Baltz</a>, <a href="http://dimainstone.com/" target="_blank">Di Mainstone</a>, and others.</li>

    <li>Designing and teaching a course called "<a href="/2017/06/designing-and-teaching-how-to-build-almost-anything-at-metropolitan-community-colleges-fablab/" aria-label="How to Build Almost Anything course">How to Build Almost Anything</a>" at Nebraska's first and only FabLab.</li>

    <li>Presenting my work at <a href="/2013/07/exhibiting-open-source-assistive-technology-at-maker-faire-kansas-city-2013/" aria-label="read write-up about my experience at Maker Faire: Kansas City">Maker Faire: Kansas City</a> and the <a href="/2013/09/open-hardware-summit-2013-experiencing-boston/" aria-label="read about my experience at the Open Hardware Summit">Open Hardware Summit</a> hosted at MIT.</li>

    <li>Developing open-source assistive technology projects such as <a href="/?s=openSip%2BPuff" aria-label="search for all posts on the open sip and puff project">openSip+Puff</a>, <a href="/?s=eyewriter" aria-label="search for all posts on the eye writer project">Eyewriter 2.1</a> and lessons on <a href="/2014/04/use-the-makey-makey-to-make-diy-assistive-technology-for-computer-access/" aria-label="lessons on using MaKey MaKey for assistive technology">using the MaKey MaKey for AT</a>.</li>

    <li><a href="/tag/glassblowing/" aria-label="search for all posts tagged with glassblowing">Collaborating with a glass artist</a> to explore applications of 3D printing with hand-blown glass.</li>

    <li>Making <a href="/tag/art-851c/" aria-label="search for all posts from my ART 851c course">3D-printed molds</a> and experimenting with <a href="/tag/casting/" aria-label="search for all posts about casting">DIY casting media</a>.</li>

    <li>Designing and teaching the University of Nebraska at Kearney's first ever <a href="/2011/07/interactive-and-generative-art-course-spring-2011/" aria-label="Interactive and Generative art course">semester-long course</a> about creative coding and physical computing to a group of fellow undergraduate students, culminating in a fun, well-attended <a href="/2011/07/2011-interactive-and-generative-art-exhibition/" aria-label="Interactive and Generative Art exhibition">public exhibition</a> of student projects.</li>

    <li>Building a pretty large and very successful <a href="/2015/01/aquaponics-experiment-2013/" aria-label="aquaponics experiment from 2013">aquaponics system</a> for under $300.</li>
  </ul>

