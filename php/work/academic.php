    <h2 class="degree__title">B.Sc. in Applied Computer Science at UNK <small class="is-pulled-right">2005 - 2011</small></h2>

    <div class="degree__body">
      <p>As an undergraduate at the University of Nebraska at Kearney I majored in Applied Computer Science and minored
        in Physics, with the intent of exploring advanced uses of technology, particularly in electronics and
        programming. Though my formal academic experience was not quite what I was hoping for, I was able to pursue my
        interests in the form of indepedent studies and extracurricular hobbies.</p>
      <p>Eventually, in the summer of 2010 I applied for and received a $25,000 grant offered by the University of
        Nebraska Foundation, enabling me to spend my final year as an undergraduate primarily focused on exploring
        these independant studies and interests. Thanks to the work I was able to pursue during this time I developed a
        deeper personal motivation to learn new skills and constantly experiment, a motivation that has been growing
        ever since.</p>

