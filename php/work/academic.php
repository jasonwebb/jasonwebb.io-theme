    <h2 class="degree__title">M.S. Ed. in Instructional Technology at UNK <small class="is-pulled-right">2011 - 2014</small></h2>

    <div class="degree__body">
      <p>In the fall of 2011, I began pursuing this degree at the University of Nebraska at Kearney's College of
        Education as a way for me to transition from traditional classroom-based learning to more self-driven,
        project-based experiential learning. This program is designed as a supplemental degree for educators in
        Nebraska and the surrounding area, and is therefore largely online-based, with very few face-to-face courses.</p>
      <p>I chose to use this to my advantage by approaching faculty from other departments on campus and working
        closely with them to explore our mutual curiosities within their disciplinary context. The following is a list
        of my academic wanderings, organized by semester.</p>

