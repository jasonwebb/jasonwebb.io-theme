          <div class="description">
            <h4>ART 853C - Printmaking</h4>

            <p>The tools of art are changing, even in traditional contexts. Increasingly, the usage of CNC milling
              machines, laser cutters and 3D printers is becoming more and more commonplace, but there are still some
              pretty substantial barriers to entry. With the exception of 3D printers, costs for these machines is
              still prohibitive for most, especially if they are not completely sure they want or need such a machine.</p>
            <p>This project was about exploring the costs and feasibility of a low-cost, hand-made CNC milling machine
              (hint: it's cheap, but difficult). This semester ended up being all about research and design, with lack
              of funding prohibiting any building.</p>

