            <h4>ART 851C - Sculpture</h4>

            <p>Working with UNK's Sculpture professor, Chad Fonfara, I pursued a series of projects in this course to
              explore the application of 3D printing within traditional mold-making and casting processes.</p>
            <p>Over the course of the semester I developed a set of parametric scripts to generate 3D-printable
              open-face and two-part molds from 3D models, which were all released on Thingiverse for free. I
              documented many experiments with casting mediums and techniques with these molds, including foods, wax,
              resin, silicone and lost ABS.</p>

