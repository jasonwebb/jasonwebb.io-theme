          <div class="description">
            <h4>ART 846 - Seminar in Art Education</h4>

            <p>In collaboration with UNK Art Education professor Dr. Stephanie Baer, I spent this semester focussing on
              the use of low-cost, open-source, DIY assistive technology in classroom environments. I became very
              interested in the possibility of applying new open-source methodologies to existing problems in art
              education for students with diverse needs. Specifically, how small-run, DIY-level OSHW can make helpful
              assistive devices available to a individuals who cannot afford commercial alternatives.</p>
            <p>This semester gave me the opportunity to pursue two interesting projects; an updated version of an
              open-source eye-tracking system called Eyewriter 2.1, and a cheap, simple "sip and puff" interface that
              can be reconfigured with minimal fuss.</p>

